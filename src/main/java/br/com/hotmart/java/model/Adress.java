package br.com.hotmart.java.model;

public class Adress {

    int id;
    String country;
    String state;
    String city;
    String street;
    String adressCode;

    public int getId() {
        return id;
    }

    public String getCountry() {
        return country;
    }

    public String getState() {
        return state;
    }

    public String getCity() {
        return city;
    }

    public String getStreet() {
        return street;
    }

    public String getAdressCode() {
        return adressCode;
    }
}
