package br.com.hotmart.java.model;

public class Endereco {

    int id;
    String pais;
    String uf;
    String cidade;
    String rua;
    String cep;

    public int getId() {
        return id;
    }

    public String getPais() {
        return pais;
    }

    public String getUf() {
        return uf;
    }

    public String getCidade() {
        return cidade;
    }

    public String getRua() {
        return rua;
    }

    public String getCep() {
        return cep;
    }
}
